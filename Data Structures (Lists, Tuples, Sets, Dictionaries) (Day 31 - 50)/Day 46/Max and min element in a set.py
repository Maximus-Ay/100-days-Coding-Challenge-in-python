'''
    This is a simply program that allows us to determine the maximum and minimum element in a set
'''

mySet = {1,2,3,4,1,4,-89,90,0-45} # Remember sets don't accept duplicates so even if you put one, it will not print it or consider it
print(mySet)
print(f"The maximum element in the set is: {max(mySet)}")
print(f"The minimum element in the set is: {min(mySet)}")
